def get_text(lang: str, key: str, **kwargs) -> str:
    lang = lang if lang in TEXTS else "zh"
    text = TEXTS[lang].get(key, TEXTS["zh"].get(key, key))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except Exception:
            pass
    return text
